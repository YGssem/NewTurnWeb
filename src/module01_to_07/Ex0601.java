package module01_to_07;

// (Ex0601) 산술연산자 (1)

public class Ex0601 {
	public static void main(String[] args) {
		int a = 1;
		int b = a + 2;
		int c = a - b;
		System.out.println("b : " + b);
		System.out.println("c : " + c);
		
		int d = b * c;
		System.out.println("d : " + d);
		System.out.println("b/a : " + b/a);
		System.out.println("b%a : " + b%a);
	}
}
