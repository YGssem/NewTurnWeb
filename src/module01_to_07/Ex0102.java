package module01_to_07;

// (Ex0102) 출력 연습

public class Ex0102 {
	public static void main(String[] args) {
		System.out.println(3);   	//→ 3이 출력됩니다. (그리고 줄바꿈)
		System.out.println("ABC"); 	//→ ABC가 출력됩니다. (그리고 줄바꿈)
		System.out.print(3); 	 	//→ 3이 출력됩니다. (줄은 안 바뀜)
		System.out.print("ABC"); 	//→ ABC가 출력됩니다. (줄은 안 바뀜)
	}
}
